console.log("Operators in Js.")

let a = 10;
let b = 5;

// Arithmetic Operator
console.log("Arithmetic Operators")

console.log("a + b = ", a + b) //Adiition
console.log("a - b = ", a - b) //Substraction
console.log("a / b = ", a / b) //Division
console.log("a * b = ", a * b) //Multiplication
console.log("a ** b = ", a ** b) //Exponent
console.log("a % b = ", a % b) //Modulus

// Assignment Operator
console.log("Assignment Operators")

console.log("a = ", a) //Value
console.log("a += b = ", a += b) //Addition
console.log("a -= b = ", a -= b) //Substraction
console.log("a *= b = ", a *= b) //Multiplication
console.log("a /= b = ", a /= b) //Division
console.log("a %= b = ", a %= b) //Modulus

// Comparison Operator
console.log("Comparison Operators")

console.log("a == b = ", a == b) //Equality
console.log("a != b = ", a != b) //Inequality
console.log("a > b = ", a > b) //Greater than
console.log("a < b = ", a < b) //Less than
console.log("a >= b = ", a >= b) //Greater than or equal to
console.log("a <= b = ", a <= b) //Less than or equal to

// Logical Operator
console.log("Logical Operators")
console.log("a && b = ", a && b) //AND
console.log("a || b = ", a || b) //OR
console.log("!(a && b) = ", !(a && b)) //NOT

// Ternary Operator
console.log("Ternary Operator")
console.log("a > b ? a : b = ", a > b ? a : b) // Ternary

// Bitwise Operator
console.log("Bitwise Operator")

console.log("a & b = ", a & b) //AND
console.log("a | b = ", a | b) //OR
console.log("a ^ b = ", a ^ b) //XOR
console.log("~a = ", ~a) //NOT
console.log("a << b = ", a << b) //Left Shift
console.log("a >> b = ", a >> b) //Right Shift
console.log("a >>> b = ", a >>> b) //Zero-fill Right Shift

// Relational Operator
console.log("Relational Operator")

console.log("a == b = ", a == b) //Equality
console.log("a != b = ", a != b) //Inequality
console.log("a > b = ", a > b) //Greater than
console.log("a < b = ", a < b) //Less than
console.log("a >= b = ", a >= b) //Greater than or equal to
console.log("a <= b = ", a <= b) //Less than or equal to

// Unary Operator
console.log("unary Operator")

console.log("a = ", a)

console.log("a++ = ", a++) //post-Increment
console.log("a = ", a) //Value

console.log("a-- = ", a--) //post-Decrement
console.log("a = ", a) //Value

console.log("++a = ", ++a) //pre-Increment
console.log("a = ", a) //Value

console.log("--a = ", --a) //pre-Increment
console.log("a = ", a) //Value
